		String email = "garam6500@naver.com";
		
		System.out.println("이름:" +name);
		System.out.println("나이:" +age);
		System.out.println("email:" +email);
		
System.out.println("---------------------------------");
		
		int num1 = 20;
		int num2 = 10;
		int addR = num1 + num2;
		int subR = num1 - num2;
		int mulR = num1 * num2;
		int divR = num1 / num2;
		System.out.println("합계:" + addR);
		System.out.println("뺄셈:" + subR);
		System.out.println("곱셈:" + mulR);
		System.out.println("나누기:" + divR);
		
		System.out.println("---------------------------------");
		
		char level = 'A';
